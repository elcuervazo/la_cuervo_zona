num1=int(input("Digite el primer valor:"))
# esta variable almacena el primer digito ingresado
num2=int(input("Digite el segundo valor:"))
# esta variable almacena el segundo digito ingresado
num3=int(input("Digite el tercer valor:"))
# esta variable almacena el tercer digito ingresado
prom= (num1+num2+num3)/3
#la variable (prom) guarda la formula del promedio la cual es la sumna de los 3 numeros ingresados, dividido por la cantidad de numero ingresados, en este caso es 3
print("El promedio de los numeros ingresados es",prom)